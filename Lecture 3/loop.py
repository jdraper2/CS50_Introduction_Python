# Initialize variable i with value 3
i = 3

# While loop will continue as long as i is not equal to 0
while i != 0:
    # Print "hello" to the console
    print("hello")
    # Decrement the value of i by 1
    i -= 1

# For loop will iterate over the list [0, 1, 2]
for i in range(10):
    # Print "Hello World" to the console
    print("Hello World")

